              <h2>占術・占いの種類</h2>
              <p>電話占いではどのような占術・種類があるのしょうか？<br>
                <br>
                占いの占術は、大きく分けて4種類に分類されます。「命術」「卜術」「相術」「霊術」になりますが、ここでは電話占いで主流となっている相術以外の3つをご紹介します。<br>
                <br>
                命術…代表的な占術「四柱推命」「ホロスコープ」「西洋・東洋占星術」「数秘術」「九星気学」「紫微斗数」など<br>
                生年月日や、生まれた年など以後も変わらないものをもとに、持って生まれた運命や才能など読み解く占術です。人間同士の相性や、持って生まれた運命や才能を知りたいとき向いている占術です。
                <br>
                卜（ボク）術…代表的な占術「タロット」「断易」「周易」「ルーン」「おみくじ」など<br>
                そのときのタイミングと偶然は、必然であるという考えの占術で、占いたいことに対してどう結果が出たかを今の現状と照らし合わせて読み解く占術です。占い師によって、出た結果をどのように解釈するか（リーディング）が変わってくるため、実績や経験が豊富な占い師に占ってもらうことが大切です。<br>
                長いスパンでの展望や、持って生まれた性格などにはあまり向かず、直近の行動の吉凶や人の気持ちなどを知りたいときなどを占いたいときに向いている占術になります。<br>
                <br><div id="sec5"></div>
                霊術…代表的な占術「霊視」「降霊術」「霊感タロット」「スピリチュアル占い」など<br>
                占い師が持っている、霊感という特別な能力で占う占術です。目に見えない能力で読み取るため信憑性という部分ではあいまいな部分もありますが、相談内容が広く対応できる占術でもあります。<br>
                霊感があるという人は数多くいますが、その能力を使って透視をおこなったり、未来を占うということは、とても難しいといわれております。見えない力だからこそ、そのときの状況によっては強く影響を受けてしまい、依存してしまうこともあります。そのため、厳しい修行を積み能力をコントロールできる占い師を見極めることがとても大切です。<br>
